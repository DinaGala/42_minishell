#include "../../include/minishell.h"

int		ft_env(t_mini *sh)
{
	unsigned int	i;

	i = 0;
	while (sh->env[i])
	{
		ft_putstr_fd(sh->env[i], 1);
		i++;
	}
	return (0);
}